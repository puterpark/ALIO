package com.kosmo.alio;

public class InfoVO {
	
	int iseq;
	int itseq;
	String ititle;
	String ibody;
	String iregdate;
	int gseq;
	
	public int getIseq() {
		return iseq;
	}
	public void setIseq(int iseq) {
		this.iseq = iseq;
	}
	public int getItseq() {
		return itseq;
	}
	public void setItseq(int itseq) {
		this.itseq = itseq;
	}
	public String getItitle() {
		return ititle;
	}
	public void setItitle(String ititle) {
		this.ititle = ititle;
	}
	public String getIbody() {
		return ibody;
	}
	public void setIbody(String ibody) {
		this.ibody = ibody;
	}
	public String getIregdate() {
		return iregdate;
	}
	public void setIregdate(String iregdate) {
		this.iregdate = iregdate;
	}
	public int getGseq() {
		return gseq;
	}
	public void setGseq(int gseq) {
		this.gseq = gseq;
	}
	
	

}
